/************************************ TIM 1 PWM CaptureCallback Function ************************************/
uint32_t IC_Val1 = 0;
uint32_t IC_Val2 = 0;
uint32_t Difference = 0;
uint8_t Is_First_Captured = 0;
uint8_t Distance = 100;
void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim) {
	/**** TIM 1 Channel 1 ****/
	if(htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1) { 							// if the interrupt source is channel 1
		if(Is_First_Captured ==0) { 																// if the first value is not captured
			IC_Val1 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1); // read the first value
			Is_First_Captured = 1; 																		// set the first captured as true
			//Now change the polarity to falling edge
			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_FALLING);
		}
		else if(Is_First_Captured == 1) { 													// if the first value is already captured
			IC_Val2 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1); // read second value
			__HAL_TIM_SetCounter(htim, 0); 														// reset the counter
			if(IC_Val2 > IC_Val1) {
				Difference = IC_Val2 - IC_Val1;
			}
			else if (IC_Val1 > IC_Val2) {
				Difference = (0xffff-IC_Val1)+IC_Val2;
			}
			Distance = Difference * .034/2;
			Is_First_Captured = 0; 																		// set it back to false
			
			// set polarity to rising edge
			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_RISING);
			__HAL_TIM_DISABLE_IT(&htim1, TIM_IT_CC1);
		}
	}
	/**** TIM 1 Channel 4 ****/
	if(htim->Channel == HAL_TIM_ACTIVE_CHANNEL_4) {
		if(Is_First_Captured ==0) { 
			IC_Val1 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_4);
			Is_First_Captured = 1; 
			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_4, TIM_INPUTCHANNELPOLARITY_FALLING);
		}
		else if(Is_First_Captured == 1) { 													// if the first value is already captured
			IC_Val2 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_4); // read second value
			__HAL_TIM_SetCounter(htim, 0); 														// reset the counter
			if(IC_Val2 > IC_Val1) {
				Difference = IC_Val2 - IC_Val1;
			}
			else if (IC_Val1 > IC_Val2) {
				Difference = (0xffff-IC_Val1)+IC_Val2;
			}
			Distance = Difference * .034/2;
			Is_First_Captured = 0; 																		// set it back to false
			
			// set polarity to rising edge
			__HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_4, TIM_INPUTCHANNELPOLARITY_RISING);
			__HAL_TIM_DISABLE_IT(&htim1, TIM_IT_CC4);
		}
	}
}

void Back_PING_Sensor_Trig(void) {
	HAL_GPIO_WritePin(GPIOC, Back_TRIG, GPIO_PIN_RESET);
	Delay_timer16_10us(5);
	HAL_GPIO_WritePin(GPIOC, Back_TRIG, GPIO_PIN_SET);
	Delay_timer16_10us(5);
	HAL_GPIO_WritePin(GPIOC, Back_TRIG, GPIO_PIN_RESET);
	__HAL_TIM_ENABLE_IT(&htim1, TIM_IT_CC1); //Enable PWM Capture Mode
}
void Front_PING_Sensor_Trig(void) {
	HAL_GPIO_WritePin(GPIOB, Front_TRIG, GPIO_PIN_RESET);
	Delay_timer16_10us(5);
	HAL_GPIO_WritePin(GPIOB, Front_TRIG, GPIO_PIN_SET);
	Delay_timer16_10us(5);
	HAL_GPIO_WritePin(GPIOB, Front_TRIG, GPIO_PIN_RESET);
	__HAL_TIM_ENABLE_IT(&htim1, TIM_IT_CC4);
}
void DHT22_Read_and_Show(void) {
	DHT22_Start();
	Presence = DHT22_Check_Response();
	Rh_byte1 = DHT22_Read();
	Rh_byte2 = DHT22_Read();
	Temp_byte1 = DHT22_Read();
	Temp_byte2 = DHT22_Read();
	SUM = DHT22_Read();
			
	TEMP = ((Temp_byte1<<8)|Temp_byte2);
	RH = ((Rh_byte1<<8)|Rh_byte2);
			
	Temperature = (float) (TEMP/10.0);
	Humidity = (float) (RH/10.0);
	Display_Temp(Temperature);
	Display_Rh(Humidity);
}

